              <p>Menuju Indonesia yang makmur dan sejahtera, dibutuhkan banyak usaha peningkatan, baik dari segi kualitas maupun kuantitas di berbagai sektor dan salah satunya ialah sektor pendidikan. Pada tahun yang terus berjalan, semakin bertambahnya populasi anak usia dini, semakin diperlukannya pula PAUD yang berkualitas dan kompeten. </p>
              <br>
              <p>
                Yayasan Cipta Asa Nusantara yang mewadahi PAUD Anak Terang berkomitmen untuk terus menyempurnakan pola pendidikan sehingga mampu mencetak generasi bangsa yang siap melanjutkan ke tingkat selanjutnya, beriman secara teguh terhadap tuhan, berkarakter dan menjadi sebuah lilin yang “Terang” bagi keluarga, lingkungan serta bangsa Indonesia.
<br>
Tak lupa pula secara beriringan, tentunya Yayasan Cipta Asa Nusantara selalu senantiasa meningkatkan kualitas guru dan karyawan, menyempurnakan sarana dan prasarana sekolah dan memiliki komitmen yang kuat dalam mengembangkan dan mendirikan PAUD ke pelosok daerah yang tingkat pendidikannya rendah serta masyarakatnya miskin atau tidak mampu.
<br>
Pada beberapa tahun ke depan, kami berencana untuk mendirikan PAUD Anak Terang di beberapa wilayah di Indonesia, antara lain :
<br>
1. Balai Karangan – Entikong, Kalimantan Barat.
<br>
2. Singkawang – Kalimantan Barat.
<br>
3. Cibubur – Bogor.
<br>
4. Mauk – Tangerang.
<br>
5. Desa Mauleum – Kec. Amanuban Timur, Kab Soe.
<br>
6. Bengkulu.
<br>
7. Kalimantan Utara.
<br>
8. Mentawai, Sumatera Barat.
<br>
Adapun rencana pembangunan juga akan ditingkatkan ke jenjang SD dan homeschooling tingkat SD. Pembangunan juga akan merambah ke asrama untuk peningkatan kualitas sumber daya manusia, yang akan di isi dengan kegiatan penguatan keahlian di berbagai bidang seperti menjahit, menyablon dan sebagainya. Kegiatan ini akan dilakukan dengan penuh keikhlasan dan tanggung jawab serta berupaya menjawab kebutuhan masyarakat akan pendidikan dan peningkatan ekonomi keluarga.
              </p>
